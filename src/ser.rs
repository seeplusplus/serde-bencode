#[test]
fn test_enum() {
    let u = 69;
    let expected = r#"i69e"#;
    assert_eq!(to_string(&u).unwrap(), expected);

    let i = -1337;
    let expected = r#"i-1337e"#;
    assert_eq!(to_string(&i).unwrap(), expected);

    let s = "LOLSTRING";
    let expected = r#"9:LOLSTRING"#;
    assert_eq!(to_string(&s).unwrap(), expected);

    let ls = vec!("lol", "this", "code", "is", "terrible");
    let expected = r#"l3:lol4:this4:code2:is8:terriblee"#;
    assert_eq!(to_string(&ls).unwrap(), expected);

    let lu = vec!(1,2,3,4,5);
    let expected = r#"li1ei2ei3ei4ei5ee"#;
    assert_eq!(to_string(&lu).unwrap(), expected);
}
